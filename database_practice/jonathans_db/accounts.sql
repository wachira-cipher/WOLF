CREATE TABLE visitor(
    id int,
    type VARCHAR(255),
    created_at VARCHAR(255),
    browser_type VARCHAR(),
    ip_address VARCHAR(255),
    total_pages VARCHAR(255),
    total_time VARCHAR(255),
    total_visits VARCHAR(255),
    name_ VARCHAR(255),
    current_visit_length VARCHAR(255),
    current_total_pages VARCHAR(255),
    company_dns_name VARCHAR(255),
    first_page_in_visit VARCHAR(255)
    );